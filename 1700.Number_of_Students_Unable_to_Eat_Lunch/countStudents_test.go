package main

import (
	"testing"
)

func TestCountStudents(t *testing.T) {
	測試案例 := []struct {
		名稱       string
		學生       []int
		三明治      []int
		期望無法用餐人數 int
	}{
		{
			名稱:       "範例1",
			學生:       []int{1, 1, 0, 0},
			三明治:      []int{0, 1, 0, 1},
			期望無法用餐人數: 0,
		},
		{
			名稱:       "範例2",
			學生:       []int{1, 1, 1, 0, 0, 1},
			三明治:      []int{1, 0, 0, 0, 1, 1},
			期望無法用餐人數: 3,
		},
		{
			名稱:       "所有學生都能用餐",
			學生:       []int{1, 1, 1, 0, 0, 1},
			三明治:      []int{1, 1, 1, 0, 0, 1},
			期望無法用餐人數: 0,
		},
		{
			名稱:       "沒有學生能用餐",
			學生:       []int{1, 1, 1},
			三明治:      []int{0, 0, 0},
			期望無法用餐人數: 3,
		},
		{
			名稱:       "空列表",
			學生:       []int{},
			三明治:      []int{},
			期望無法用餐人數: 0,
		},
	}

	for _, 案例 := range 測試案例 {
		t.Run(案例.名稱, func(t *testing.T) {
			結果 := countStudents(案例.學生, 案例.三明治)
			if 結果 != 案例.期望無法用餐人數 {
				t.Errorf("期望 %d 名學生無法用餐，但得到 %d", 案例.期望無法用餐人數, 結果)
			}
		})
	}
}
